package com.snsystems;

public class Premitives_Wrappers 
{

	/**
	 * @param args
	 */

	static char c;
	static int i;
	static float f;
	static double d;
	static long l;

	static String s;
	static Integer in;
	static Float fl;
	static Double db;
	static Long ll;

	public static void main(String[] args) {
		System.out.println("premitives");
		System.out.println("char c:" + c);
		System.out.println("int i:" + i);
		System.out.println("float f:" + f);
		System.out.println("double d:" + d);
		System.out.println("long l :" + l);

		System.out.println("\n\nWrapers");
		System.out.println("String s :" + s);
		System.out.println("Integer in:" + in);
		System.out.println("Float fl:" + fl);
		System.out.println("Double db:" + db);
		System.out.println("Long ll :" + ll);
	}

}
